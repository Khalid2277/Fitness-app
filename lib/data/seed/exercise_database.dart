/// Look up an exercise by its unique [id].
Exercise? findExerciseById(String id) {
  try {
    return exerciseDatabase.firstWhere((e) => e.id == id);
  } catch (_) {
    return null;
  }
}

/// Return exercises whose [primaryMuscle] matches [muscle] or have it as secondary.
List<Exercise> exercisesForMuscle(MuscleGroup muscle) {
  return exerciseDatabase
      .where((e) =>
          e.primaryMuscle == muscle || e.secondaryMuscles.contains(muscle))
      .toList();
}

/// Return exercises filtered by [equipment].
List<Exercise> exercisesForEquipment(EquipmentType equipment) {
  return exerciseDatabase.where((e) => e.equipment == equipment).toList();
}

/// Search exercises by name (case-insensitive substring match).
List<Exercise> searchExercises(String query) {
  final q = query.toLowerCase();
  return exerciseDatabase
      .where((e) =>
          e.name.toLowerCase().contains(q) ||
          e.primaryMuscle.displayName.toLowerCase().contains(q) ||
          e.description.toLowerCase().contains(q))
      .toList();
}
